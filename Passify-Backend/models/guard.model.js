// guard.model.js
import { Schema, model } from "mongoose";

const guardSchema = new Schema({
  username: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  name: {
    type: String,
    required: true,
  },
});

export default model("Guard", guardSchema);
